package com.java;

public class Swap {
	public static void main(String args[])
	
	{
		int a=5,b=6,temp;
		System.out.println("Before swap ");	
		System.out.println(a);
		System.out.println(b);

		temp=a;
		a=b;
		b=temp;
		System.out.println("After swap ");	
		System.out.println(a);
		System.out.println(b);
	}

}
